sum = 0
positiveNum = 0
number = int(input("Enter a series of values (99999 to quit): "))

while number != 99999:
  if number > 0:
    sum = number + sum
    positiveNum = positiveNum + 1
  number = int(input())

if positiveNum == 0:
  print("The average of the postive values is 0")
else:
  average = sum / positiveNum
  print(f"The average of the positive values is {average}")